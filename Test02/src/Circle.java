
public class Circle {
	private int x;
	public int getX() {
		return x;
	}
	public Circle() {
		super();
	}
	public Circle(int x, int y) {
		super();
		this.x = x;
		this.y = y;
	}
	public void setX(int x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}
	public int getRadius() {
		return radius;
	}
	public void setRadius(int radius) {
		this.radius = radius;
	}
	private int y;
	private int radius;

}
